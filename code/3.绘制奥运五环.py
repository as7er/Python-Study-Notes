#绘制奥运五环

import turtle
turtle.width(15)
turtle.color('blue')
turtle.circle(70)
turtle.penup()
turtle.goto(160,0)
turtle.color('black')
turtle.pendown()
turtle.circle(70)
turtle.penup()
turtle.goto(320,0)
turtle.color('red')
turtle.pendown()
turtle.circle(70)
turtle.penup()
turtle.goto(80,-70)
turtle.color('yellow')
turtle.pendown()
turtle.circle(70)
turtle.penup()
turtle.goto(240,-70)
turtle.color('green')
turtle.pendown()
turtle.circle(70)


